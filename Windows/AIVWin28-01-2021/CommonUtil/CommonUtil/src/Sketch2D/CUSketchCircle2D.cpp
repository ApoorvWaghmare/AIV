#include "CUSketchCircle2D.h"

//------------------------------------------------------------------------------------------------------------------
namespace CommonUtil
{
	SketchCircle2D::SketchCircle2D(const CommonUtil::Point2D& center, float radius)
	{
		m_center = center;
		m_radius = radius;
	}

	//------------------------------------------------------------------------------------------------------------------

	SketchCircle2D::~SketchCircle2D()
	{}

	//------------------------------------------------------------------------------------------------------------------

	const CommonUtil::Point2D& SketchCircle2D::GetCenter()const
	{
		return m_center;
	}

	//------------------------------------------------------------------------------------------------------------------

	float SketchCircle2D::GetRadius()const
	{
		return m_radius;
	}

	//------------------------------------------------------------------------------------------------------------------

	void SketchCircle2D::SetCenter(const CommonUtil::Point2D& center)
	{
		m_center = center;
	}
	
	//------------------------------------------------------------------------------------------------------------------

	void SketchCircle2D::SetRadius(float radius)
	{
		m_radius = radius;
	}
}
